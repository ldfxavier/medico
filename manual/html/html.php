<h1>HTML</h1>
<div class="margin_20"></div>
<p>Regras para montagem do HTML na estruturação do site. Todo o HTML do site deve ficar no diretório <b>App/Views/</b>.</p>
<p>A estruturação do HTML deve seguir a seguintes regras:</p>

<ul>
	<li>Deve começar com um bloco contendo um ID que será composto por "bloco__{controller}--{action}";</li>
	<li>Deve usar o mínimo de div possível;</li>
	<li>Deve usar tags para a melhor semantica possível;</li>
	<li>Não pode abreviar as classes;</li>
	<li>Deve separar o html em blocos;</li>
	<li>Cada bloco deve começar com uma classe ".bloco_{descricao}";</li>
	<li>Deve usar os nomes no singular;</li>
	<li>Deve usar os nomes no masculino;</li>
	<li>Não deve ter PHP, JS, CSS e qualquer outra linguagem fora o HTML;</li>
	<li>Colocar os scripts no final da página;</li>
</ul>

<h3>Exemplo:</h3>
<script src="https://gist.github.com/marktclub/7a5730d2307103fa66b081e06f378e6a.js"></script>


